     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $employees=new Employees();
        $employees->name=$request->name;
        $employees->lastname=$request->lastname;
        $employees->age=$request->age;
        $employees->gender=$request->gender;
        $employees->id_departament=$request->id_departament;
        $employees->contract_date=$request->contract_date;
        $employees->save();
        //metodo post nos guarda un nuevo empleado en la BBDD
    }

